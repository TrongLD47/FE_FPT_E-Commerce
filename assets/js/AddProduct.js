
function checkSizeXs() {
    var sizeXS = document.getElementById("sizeXs");
    var textXS = document.getElementById("textXs");
    if (sizeXS.checked == true) {
        textXS.style.display = "block";
    } else {
        textXS.style.display = "none";
    }
}



